// functions in js are block of code that performes a specific task, can be invoked (called)
// whenever needed.  functions make our safe from redundancy (repeat).




// (1) Defination
function funName(p1,p2){  //parameter  // func parameters like local variables , have block scop of func.
    //do some work

    return // it returns so we can get after function.

    console.log();  // unreachable code after return.
};


// (2)  call
funName(p1, p2);  //argument.









